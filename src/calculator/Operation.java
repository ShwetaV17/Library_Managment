package calculator;

public class Operation
{
    public static int sum(int Num1, int Num2)
    {
        int su = Num1 + Num2 ;
        return su;
    }
    public static int sub(int Num1, int Num2)
    {
        int sub = Num1 - Num2 ;
        return sub;
    }
    public static int mul(int Num1, int Num2)
    {
        int mul = Num1 * Num2 ;
        return mul;
    }
    public static int div(int Num1, int Num2)
    {
        int div = Num1 / Num2 ;
        return div;
    }

}
